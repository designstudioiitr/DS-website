function circle_scroller(content){
  for(var i=0; i<content.length; i++){
    $( "#scroller-circle" ).append( "<div class=\"circular-scrolls\" id=\""+i+"\"></div>" );
  }
  $("#scroller-circle > #0").addClass("filled-scrolls");
}

function circle_scroller_updater(content, count){
  $(".circular-scrolls").removeClass("filled-scrolls");
  $(".circular-scrolls").attr('style','');
  var data = JSON.parse(content[count%content.length]);
  $("#scroller-circle > #"+count%content.length).addClass("filled-scrolls");
  $(".filled-scrolls").css({"background-color":data.color});
}

$(document).ready(function(){
  $('#submit_form').submit(function(event) {
  event.preventDefault();
  $("#loader-container").css({'display':'inherit'});
  $(".content").css({'opacity':'0.25'});
  var formData = $('#submit_form').serialize();

  console.log("Sending Mail")

  $.ajax({
  type: 'POST',
  url: $('#submit_form').attr('action'),
  data: formData,
  dataType: 'json'
  }).done(function(response) {
    if(response.status == 200){
      $("#loader-container").css({'display':'none'});
      $("#sent").fadeIn(200).promise().done(function(){$(".content").css({'opacity':'1'})});
      $("#sent").fadeOut(1000);
      $('#submit_form #name').val('');
      $('#submit_form #email').val('');
      $('#submit_form #message').val('');
    }
    else {
      $("#loader-container").css({'display':'none'});
      $("#not-sent").fadeIn(200).promise().done(function(){$(".content").css({'opacity':'1'})});
      $("#not-sent").fadeOut(1000);
      $('#submit_form #name').val('');
      $('#submit_form #email').val('');
      $('#submit_form #message').val('');
    }});
  });
});

function preload(content) {
  var images = [];
  for(var i=0; i<content.length; i++){
    var data = JSON.parse(content[i]);
    images.push("/static/images/"+data.identifier+".png")
  }
  $(images).each(function () {
      $('<img />').attr('src',this).appendTo('body').css('display','none');
  });
}
