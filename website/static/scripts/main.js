function content_loader() {
  var content = null;
  $.ajax({
        url: "/content_loader/",
        type: 'get',
        dataType: 'json',
        async: false,
        success: function(data) {
            content = data;
        }
    });
  return content;
}

function translateDown() {
  $('#project').css({"transition": "unset","transform": "translateY(700px)"});
  setTimeout(function() {$('#project').css({"transition": "all 0.5s","transform": "translateY(0px)"});},20);
  $('.w-portfolio .left').slideDown(400);
}

function translateUp() {
  $('#project').css({"transform" : "translateY(-700px)"});
  $('.w-portfolio .left').slideUp(400);
}

function content_updater(content, count=0) {
  var toserve = count % content.length;
  var data = JSON.parse(content[toserve]);

  function change() {
    $('.w-portfolio .left .heading').text(data.name);
    $('.w-portfolio .left .sub-heading span').text(data.subhead);
    $('.w-portfolio .left .description').text(data.brief)
    $('.w-portfolio .right #project').attr("src","/static/images/"+data.identifier+".png");
    $('.coloured').css({"color":data.color});
    $('.coloured-bg').css({"background": "radial-gradient("+data.end_color+","+data.color+")"});
    $('.coloured-icon').css({"background": "linear-gradient("+data.color+","+data.end_color+")","-webkit-background-clip": "text","-webkit-text-fill-color": "transparent"});
    $('#footer-grad > #start').css({"stop-color":data.end_color, "stop-opacity":"1"});
    $('#footer-grad > #stop').css({"stop-color":data.color, "stop-opacity":"1"});
    $('.view-project > a').attr("href","/projects?name="+data.identifier);
    $('.w-portfolio .right > a').attr("href","/projects?name="+data.identifier);
  }

  async function three() {
	  await translateUp();
    await setTimeout(function(){change()}, 520);
  }
  async function animate() {
    await three();
    await setTimeout(function(){translateDown()},540);
  }
  animate();

};

function activeStateManager(explicit = "none") {
  $('nav a').removeClass('coloured active');
  if(explicit!="none") {$('#nav-' + explicit).addClass('coloured active'); return 0;}
  var loc = $(location).attr("href").split("/").pop();
  loc = loc.replace("#","");

  if(loc.substring(0,7)!="project") {$('#nav-'+loc).addClass('coloured active');}
}

